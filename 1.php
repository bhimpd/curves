<script>// JavaScript code for pagination
var pageLinks = document.querySelectorAll('.page-link');
var currentPage = 1;

for (var i = 0; i < pageLinks.length; i++) {
  pageLinks[i].addEventListener('click', function() {
    if (this.innerHTML === 'Previous') {
      if (currentPage > 1) {
        currentPage--;
      }
    } else if (this.innerHTML === 'Next') {
      if (currentPage < pageLinks.length - 2) {
        currentPage++;
      }
    } else {
      currentPage = parseInt(this.innerHTML);
    }
    
    // Update active page link
    for (var j = 0; j < pageLinks.length; j++) {
      pageLinks[j].parentNode.classList.remove('active');
    }
    pageLinks[currentPage].parentNode.classList.add('active');
  });
}
</script>


